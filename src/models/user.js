const { Schema, model } = require("mongoose");

const MySchema = new Schema({
  username: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now(),
  },
});

const UserModel = model("user", MySchema);

module.exports = UserModel;
